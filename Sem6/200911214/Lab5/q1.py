num1 = input('Enter a number: ')
num2 = input('Enter a number again: ')
op = input('enter an operand: ')
num1 = float(num1)
num2 = float(num2)
if(op == '+'):
    print(num1+num2)
if(op == '-'):
    print(num1-num2)
if(op == '*'):
    print(num1*num2)
if(op == '/'):
    print(num1/num2)
if(op == '%'):
    print(num1%num2)

